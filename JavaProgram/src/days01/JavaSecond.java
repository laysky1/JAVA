package days01;

public class JavaSecond {

	public static void main(String[] args) {
		System.out.printf("1\t겁나 쉬운 \"자바\" 프로그래밍\n");
		System.out.printf("12\t겁나 쉬운 \'자바\' 프로그래밍\n");
		System.out.printf("123\t겁나 쉬운 '자바' 프로그래밍\n");
		System.out.printf("123456\t겁나 쉬운 \\자바\\ 프로그래밍\n");
		System.out.printf("12345\t겁나 쉬운 %%자바%% 프로그래밍\n");
		
		/*
		 		 \n : 한줄 개행
		 		 \t : tab 을 누른것처럼 일정 간격으로 커서가 이동합니다.
		 		 \" : " " 사이에서 "를 사용해서 강조하고하는 단어가 있을때 사용
		 		 \' : 작은 따옴표로 강조할때 사용.  현재는 사용하지 않아도 무방합니다
		 		 \\ : 백슬래쉬 출력
		 		 %% : % 출력
		 		 \b : 백스페이스 - 커서 왼쪽으로 이동하면서 앞글자를 한글자씩 삭제합니다 
		 		 						다만 이클립스 콘솔창에서 현재font로는 정상작동하지 않습니다. 
		*/
	}
}
